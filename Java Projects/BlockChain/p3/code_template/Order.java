/**
 * An enumeration representing different types of binary tree traversal orders.
 */
public enum Order {
    /**
     * Represents the pre-order traversal order in a binary tree.
     */
    PREORDER,

    /**
     * Represents the in-order traversal order in a binary tree.
     */
    INORDER,

    /**
     * Represents the post-order traversal order in a binary tree.
     */
    POSTORDER;
}

